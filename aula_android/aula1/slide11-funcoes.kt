// funcao simples
fun novaFuncao() {
    printl("oi, tudo bem?")
}

// função privado com passagem de parametro tipado
private fun novaFuncao2(nome: String) {
    printl("Oi, $nome")
}

fun main() {
    novaFuncao()
    novaFuncao2()
}

